    {
//        return TaskResource::collection(Task::all());
        $tasks = Task::with(['owner', 'assignee'])
            ->assignedToMe()
            ->createdByMe()
            ->orderBy('priority', 'desc')
            //->orderBy('due_date', 'desc')
